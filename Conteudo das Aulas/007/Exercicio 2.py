"""
Faça um Programa que pergunte quanto você ganha por hora, e o número de horas
trabalhadas no mês.
Calcule e mostre o total do seu salário no referido mês.
"""

vlh = int(input('Digite o seu valor hora: '))
hrm = int(input('Quantas Horas você trabalhou este mês?: '))

print ('Seu Salario este mes é: ', float(vlh * hrm))